    int c = (2 + 3)/5 - 8 * (3 + 123) % 10 - 2 * (10 + 123)/10;
    cout << "3. (2 + 3)/5 - 8 * (3 + 123) % 10 - 2 * (10 + 123)/10 = " << c << endl;

