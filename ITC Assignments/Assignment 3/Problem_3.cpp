#include <iostream>
using namespace std;

int main()
{
    cout << "Surface Area of Cube: "
         << 6 * 0.25 * 0.25 << endl;

    cout << "Surface Area of Cylinder: "
         << (44 / 7.0) * 0.25 * 0.25 * 0.25 << endl;

    cout << "Surface Area of Sphere: "
         << (88 / 7.0) * 0.25 * 0.25 * 0.25 << endl;

    return 0;
}
